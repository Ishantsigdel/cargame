def sum(a,b):
    result = a + b
    print("result", result)
    return result

def diff(a,b):
    result = a - b
    print("result", result)
    return result

def product(a,b):
    result = a * b
    print("result", result)
    return result

def divide(a,b):
    result = a / b
    print("result", result)
    return result

def modulus(a,b):
    result = a % b
    print("result", result)
    return result

def floordiv(a,b):
    result = a // b
    print("result", result)
    return result